public class Loops {

    public static void main(String[] args) {

        int n=5;


        while(n!=0){
            System.out.println(n);
            n--;
        }
        System.out.println("");


        n=5;
        for(int i=0; i<n; i++){
            System.out.println(i);
        }

        System.out.println("");

        n=5;

        do{
            System.out.println(n);
            n--;
        }while(n!=0);


    }
    
}
